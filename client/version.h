/**
 * @file version.h
 * @brief noxssh application version (single source of truth).
 *
 * Update these values for each release. Version is shown in -h/--help
 * and can be used by the SSH client identification string.
 */

#ifndef NOXSSH_VERSION_H
#define NOXSSH_VERSION_H

/** @brief Major version number. */
#define NOXSSH_VERSION_MAJOR 0
/** @brief Minor version number. */
#define NOXSSH_VERSION_MINOR 2
/** @brief Patch version number. */
#define NOXSSH_VERSION_PATCH 25

/** @brief Version string for display (e.g. "0.1.0"). */
#define NOXSSH_VERSION_STRING "0.2.25"

#endif /* NOXSSH_VERSION_H */
